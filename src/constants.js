module.exports = {
  pi: Math.PI,
  sqrt: (num) => {
    return Math.sqrt(num);
  },
  sq: (num) => {
    return num ** 2;
  },
  cube: (num) => {
    return Math.pow(num, 3);
  },
  sin: (ang) => {
    return Math.sin(ang);
  },
  cos: (ang) => {
    return Math.cos(ang);
  },
  tan: (ang) => {
    return Math.tan(ang);
  },
};
